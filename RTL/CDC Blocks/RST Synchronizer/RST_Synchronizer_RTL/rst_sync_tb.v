`timescale 1ns/1ps ;

module rst_sync_tb ();
 
////////////////////////////////////////////////////////
/////////////////// parameters  //////////////////////// 
////////////////////////////////////////////////////////
parameter NUM_STAGES_TB = 2 ;

////////////////////////////////////////////////////////
/////////////////// DUT signals ////////////////////////
////////////////////////////////////////////////////////
reg clk_tb ; 
reg rst_tb ; 
wire sync_rst_tb;



////////////////////////////////////////////////////////
////////////////// initial block /////////////////////// 
////////////////////////////////////////////////////////
initial
begin
	$dumpfile("rst_sync.vcd");       
	$dumpvars; 
 
clk_tb = 1'b0 ;
rst_tb = 1'b1 ;


#7
rst_tb = 1'b0 ;    

#5
rst_tb = 1'b1 ; 

#135
rst_tb = 1'b0 ;    

#5
rst_tb = 1'b1 ; 



#100 ;
$stop ;
    
end  

////////////////////////////////////////////////////////
/////////////////// RX_CLK generation //////////////////
////////////////////////////////////////////////////////

always #5 clk_tb = ! clk_tb ;

////////////////////////////////////////////////////////
/////////////////// DUT Instantation ///////////////////
////////////////////////////////////////////////////////
					 
rst_sync # ( .NUM_STAGES(NUM_STAGES_TB) )  DUT
(
.clk(clk_tb),
.rst(rst_tb),
.sync_rst(sync_rst_tb)
);

endmodule
